package day17_While_DoWhile;

public class WhileLoopIntro {
    public static void main(String[] args) {
        if(true){
            System.out.println("Ayse");//1 time executed
        }
        System.out.println("---------------------------------------");
        while(true){
            System.out.println("Ayse");//infinite time will be executed:repeated if statement
        }

    }
}
